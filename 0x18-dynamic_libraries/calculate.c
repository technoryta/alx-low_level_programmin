#include <stdio.h>
/**
 * add - adds two numbers
 * @a: int
 * @b: int
 * Return: int
*/
int add(int a, int b)
{
return (a + b);
}

/**
 * sub - adds two numbers
 * @a: int
 * @b: int
 * Return: int
*/
int sub(int a, int b)
{
return (a - b);
}

/**
 * mul - adds two numbers
 * @a: int
 * @b: int
 * Return: int
*/
int mul(int a, int b)
{
return (a * b);
}

/**
 * div - adds two numbers
 * @a: int
 * @b: int
 * Return: int
*/
int div(int a, int b)
{
return (a / b);
}

/**
 * mod - adds two numbers
 * @a: int
 * @b: int
 * Return: int
*/
int mod(int a, int b)
{
return (a % b);
}
